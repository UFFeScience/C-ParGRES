SELECT A.CODPAIS,
A.sidra_0133_19_01  AS sidra_0133_19,
A.sidra_0002_02_00  AS sidra_0002_02,
A.sidra_11913_05_00 AS sidra_11913_05,
A.sidra_11915_04_00 AS sidra_11915_04,
SUM(A.PESOPESS) AS V916 
FROM CD2010APESS A
WHERE A.CODANOPESQ = 2010 AND (A.V6036 >= 10 AND A.CODV6910 = 1)
GROUP BY 
A.CODPAIS, A.sidra_0133_19_01, A.sidra_0002_02_00, A.sidra_11913_05_00, A.sidra_11915_04_00
HAVING (A.sidra_0133_19_01 <> -1 AND A.sidra_0002_02_00  <> -1 AND A.sidra_11913_05_00 <> -1 AND A.sidra_11915_04_00 <> -1);